/****************************************************************************
*
*  Function Name: csl_caph_Mixer_GainMapping2_t csl_caph_gain_GetMixerGain2(Int16 gain)
*
*  Description: read the mixer output coarse gain.
*
*  Note: In register SRC_SPK1_LT_GAIN_CTRL1: 
*   Bit Field    Field Name                   Default     Description 
*   26:24       SPK1_LT_BIT_SELECT   0x0          Magnifies the input by BIT_SELECT*6.02dB. Default value is 0.
*
*  
*  @param  gain                  Requested gain in Q13.2.
*                                        mixer output coarse gain.
*
*  @return   unsigned int 	the bitfield value used in SRC_SPK1_LT_GAIN_CTRL1:SPK1_LT_BIT_SELECT
*
****************************************************************************/
unsigned long csl_caph_gain_GetMixerOutputCoarseGain(Int16 gain_quarterDB )
{
    if(gain_quarterDB<=20)
    	return 0;
	else if( gain_quarterDB>20 && gain_quarterDB<=44 )  // (1 * 6.02 * 4) = 6.02 dB = 24.08 qdB
		return 1;
	else if( gain_quarterDB>44 && gain_quarterDB<=68 ) // (2 * 6.02 * 4) = 12.04 dB = 48.16 qdB
		return 2;
	else if( gain_quarterDB>68 && gain_quarterDB<=92 ) // (3 * 6.02 * 4) = 18.06 dB = 72.24 qdB
		return 3;
	else if( gain_quarterDB>92 && gain_quarterDB<=116 ) // (4 * 6.02 * 4) = 24.08 dB = 96.32 qdB
		return 4;
	else if( gain_quarterDB>112 && gain_quarterDB<=140 ) //(5 * 6.02 * 4) = 30.10 dB = 120.4 qdB
		return 5;
	else if( gain_quarterDB>140 && gain_quarterDB<=164 )  //(6 * 6.02 * 4) = 36.12 dB = 144.48 qdB
		return 6;
	else if( gain_quarterDB >= 164 )           //(7 * 6.02 * 4) = 42.14 dB = 168.56 qdB
		return 7;
	
	return 0; //to avoid compile warning
}


