/****************************************************************************
*
*  Function Name: csl_caph_MicDSP_Gain_t csl_caph_gain_GetMicDSPGain(
*                                         csl_caph_MIC_Path_e mic, UInt16 gain)
*
*  Description: read the mic path gains when DSP is involved.
*
****************************************************************************/
csl_caph_MicDSP_Gain_t csl_caph_gain_GetMicDSPGain(csl_caph_MIC_Path_e mic, Int16 gain)
{
    csl_caph_MicDSP_Gain_t outGain;
    csl_caph_MicDSP_Gain_t *MicDSP_GainTable = NULL;
    UInt8 i = 0;
    UInt8 maxNum = 0;
    memset(&outGain, 0, sizeof(csl_caph_MicDSP_Gain_t));
    _DBG_(Log_DebugPrintf(LOGID_SOC_AUDIO, "csl_caph_gain_GetMicDSPGain::mic=%d, gain=0x%x\n", mic, gain));
    if (mic == MIC_ANALOG_HEADSET)
    {
	    maxNum = AMIC_GAIN_LEVEL_NUM;
	    MicDSP_GainTable=AMicDSP_GainTable;
    }
    if (mic == MIC_DIGITAL)
    {
	    maxNum = DMIC_GAIN_LEVEL_NUM;
	    MicDSP_GainTable=DMicDSP_GainTable;
    }
    if (gain >= MicDSP_GainTable[maxNum-1].micGain)
    {
        memcpy(&outGain, &MicDSP_GainTable[maxNum-1], sizeof(csl_caph_MicDSP_Gain_t));
        _DBG_(Log_DebugPrintf(LOGID_SOC_AUDIO, "micPGA=0x%x, micCICFineScale=0x%x, micCICBitSelect=0x%x, micDSPULGain=0x%x\n", outGain.micPGA, outGain.micCICFineScale, outGain.micCICBitSelect, outGain.micDSPULGain));
        return outGain;	    
    }
    else
    if (gain <= MicDSP_GainTable[0].micGain)
    {
        memcpy(&outGain, &MicDSP_GainTable[0], sizeof(csl_caph_MicDSP_Gain_t));
        _DBG_(Log_DebugPrintf(LOGID_SOC_AUDIO, "micPGA=0x%x, micCICFineScale=0x%x, micCICBitSelect=0x%x, micDSPULGain=0x%x\n", outGain.micPGA, outGain.micCICFineScale, outGain.micCICBitSelect, outGain.micDSPULGain));
        return outGain;	    
    }
    
    for (i = 0; i<maxNum; i++)
    {
        if(gain == MicDSP_GainTable[i].micGain)
        {
            memcpy(&outGain, &MicDSP_GainTable[i], sizeof(csl_caph_MicDSP_Gain_t));
            _DBG_(Log_DebugPrintf(LOGID_SOC_AUDIO, "micPGA=0x%x, micCICFineScale=0x%x, micCICBitSelect=0x%x, micDSPULGain=0x%x\n", outGain.micPGA, outGain.micCICFineScale, outGain.micCICBitSelect, outGain.micDSPULGain));
            return outGain;	    
	}	
	else
        if((gain > MicDSP_GainTable[i].micGain)
          &&(gain <= MicDSP_GainTable[i+1].micGain))
        {
            memcpy(&outGain, &MicDSP_GainTable[i+1], sizeof(csl_caph_MicDSP_Gain_t));
            _DBG_(Log_DebugPrintf(LOGID_SOC_AUDIO, "micPGA=0x%x, micCICFineScale=0x%x, micCICBitSelect=0x%x, micDSPULGain=0x%x\n", outGain.micPGA, outGain.micCICFineScale, outGain.micCICBitSelect, outGain.micDSPULGain));
            return outGain;	    
	}	
    }
    //Should not run to here.
    audio_xassert(0,0);
    return outGain;
}


