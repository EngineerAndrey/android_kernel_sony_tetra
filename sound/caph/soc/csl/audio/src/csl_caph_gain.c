/****************************************************************************
*
*  Function Name: csl_caph_Mic_Gain_t csl_caph_gain_GetMicGain(
*                                         csl_caph_MIC_Path_e mic, UInt16 gain)
*
*  Description: read the mic path gains.
*
****************************************************************************/
csl_caph_Mic_Gain_t csl_caph_gain_GetMicGain(csl_caph_MIC_Path_e mic, Int16 gain)
{
    csl_caph_Mic_Gain_t outGain;
    csl_caph_Mic_Gain_t *Mic_GainTable = NULL;
    UInt8 i = 0;
    UInt8 maxNum = 0;
    memset(&outGain, 0, sizeof(csl_caph_Mic_Gain_t));
    _DBG_(Log_DebugPrintf(LOGID_SOC_AUDIO, "csl_caph_gain_GetMicDSPGain::mic=%d, gain=0x%x\n", mic, gain));
    if (mic == MIC_ANALOG_HEADSET)
    {
	    maxNum = AMIC_GAIN_LEVEL_NUM;
	    Mic_GainTable=&AMic_GainTable[0];
    }
    else
    if (mic == MIC_DIGITAL)
    {
	    maxNum = DMIC_GAIN_LEVEL_NUM;
	    Mic_GainTable=&DMic_GainTable[0];
    }

    if (gain >= Mic_GainTable[maxNum-1].micGain)
    {
        memcpy(&outGain, &Mic_GainTable[maxNum-1], sizeof(csl_caph_Mic_Gain_t));
        _DBG_(Log_DebugPrintf(LOGID_SOC_AUDIO, "micPGA=0x%x, micCICFineScale=0x%x, micCICBitSelect=0x%x, micDSPULGain=0x%x\n", outGain.micPGA, outGain.micCICFineScale, outGain.micCICBitSelect, outGain.micDSPULGain));
        return outGain;	    
    }
    else
    if (gain <= Mic_GainTable[0].micGain)
    {
        memcpy(&outGain, &Mic_GainTable[0], sizeof(csl_caph_Mic_Gain_t));
        _DBG_(Log_DebugPrintf(LOGID_SOC_AUDIO, "micPGA=0x%x, micCICFineScale=0x%x, micCICBitSelect=0x%x, micDSPULGain=0x%x\n", outGain.micPGA, outGain.micCICFineScale, outGain.micCICBitSelect, outGain.micDSPULGain));
        return outGain;	    
    }
    
    for (i = 1; i<maxNum-1; i++)
    {
        if(gain == Mic_GainTable[i].micGain)
        {
            memcpy(&outGain, &Mic_GainTable[i], sizeof(csl_caph_Mic_Gain_t));
            _DBG_(Log_DebugPrintf(LOGID_SOC_AUDIO, "micPGA=0x%x, micCICFineScale=0x%x, micCICBitSelect=0x%x, micDSPULGain=0x%x\n", outGain.micPGA, outGain.micCICFineScale, outGain.micCICBitSelect, outGain.micDSPULGain));
            return outGain;	    
    	}	
    }
    //Should not run to here.
    audio_xassert(0,0);
    return outGain;
}


